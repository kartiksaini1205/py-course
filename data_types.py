#single valued data types
age = 30            # integer
height = 5.9       # float
is_std = True   # boolean
complex_num = 3 + 4j  # complex number

#multivalued data types
str="Hello, World!"  # string
li = [1, 2, 3, 4, 5]  # list
tup = (10.0, 20.0)  # tuple
dictionary = {'name': 'Kartik', 'age': 20}  # dictionary
num = {1, 2, 3, 4}  # set

print(age, height, is_std, complex_num, str, li, tup, dictionary, num)

